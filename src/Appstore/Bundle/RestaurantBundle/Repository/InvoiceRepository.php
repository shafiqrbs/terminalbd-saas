        if($entity->getTransactionMethod()->getSlug() == 'mobile' and !empty($entity->getAccountMobileBank()) and !empty($entity->getAccountMobileBank()->getServiceCharge())){
            $serviceCharge = $entity->getAccountMobileBank()->getServiceCharge();
