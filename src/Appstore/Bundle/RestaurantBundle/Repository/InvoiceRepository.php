    public function discountCalculation(Invoice $invoice,$discount)
    {
        $type = $invoice->getRestaurantConfig()->getDiscountType();
        if($type == 'flat'){
            $val = ( $invoice->getSubTotal() - $discount );
        }else{
            $val = ( ($invoice->getSubTotal() * (int)$discount)/100 );
        }
        return round($val);
    }

