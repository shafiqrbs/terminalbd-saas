                    }
                }
                if(isset($item['paymentCardNo']) and $item['paymentCardNo']) {
                    $sales->setCardNo($item['paymentCardNo']);
                }
                if(isset($item['transactionId']) and $item['transactionId']) {
                    $sales->setCardNo($item['transactionId']);
                }
